      refreshOnceTasksEvent(rsp.onceTasks);
      refreshDailyTasksEvent(rsp.dailyTasks);
      refreshWeeklyTasksEvent(rsp.weeklyTasks);

      totalTasks = 0;
      totalCompleteTask = 0;
      totalRewardCoins = 0;
      totalRewardCards = 0;


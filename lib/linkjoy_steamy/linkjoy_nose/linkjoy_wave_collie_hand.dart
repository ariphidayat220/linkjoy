  Future<void> _loadDailyData() async {
    LinkjoyOldenFreaked? rsp = await LINKJOY.http.rest(
      10200,
      {"type": 2},
      (p0) => LinkjoyOldenFreaked.fromJson(p0),
      autoToastOnError: true,
      showLoadingUI: true,
    );
